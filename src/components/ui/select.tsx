// Combobox component for searchable select
interface ComboboxProps {
  options: readonly string[];
  value?: string;
  onValueChange: (value: string) => void;
  placeholder?: string;
  emptyMessage?: string;
  className?: string;
  triggerClassName?: string;
}

const Combobox = ({
  options,
  value,
  onValueChange,
  placeholder = "Select option",
  emptyMessage = "No results found.",
  className,
  triggerClassName,
}: ComboboxProps) => {
  const [open, setOpen] = React.useState(false);
  const [searchQuery, setSearchQuery] = React.useState("");

  // Enhanced safety checks for options
  const safeOptions = React.useMemo(() => {
    if (!options) return [] as string[];
    if (!Array.isArray(options)) return [] as string[];
    return options;
  }, [options]);

  // Filter options based on search query
  const filteredOptions = React.useMemo(() => {
    if (safeOptions.length === 0) return [] as string[];
    
    if (searchQuery === "") return safeOptions;
    
    return safeOptions.filter((option) => 
      option.toLowerCase().includes(searchQuery.toLowerCase())
    );
  }, [safeOptions, searchQuery]);

  // Render a simple input field if there are no options
  if (safeOptions.length === 0) {
    return (
      <input
        type="text"
        value={value || ""}
        onChange={(e) => onValueChange(e.target.value)}
        placeholder={placeholder}
        className={cn(
          "flex h-10 w-full rounded-md border border-input bg-background px-3 py-2 text-sm ring-offset-background placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2 disabled:cursor-not-allowed disabled:opacity-50",
          className
        )}
      />
    );
  }

  // Ensure we have a non-empty array of filtered options for the Command component
  const commandItems = filteredOptions.length > 0 
    ? filteredOptions.map((option) => (
        <CommandItem
          key={option}
          value={option}
          onSelect={() => {
            onValueChange(option);
            setSearchQuery("");
            setOpen(false);
          }}
        >
          <Check
            className={cn(
              "mr-2 h-4 w-4",
              value === option ? "opacity-100" : "opacity-0"
            )}
          />
          {option}
        </CommandItem>
      ))
    : [<CommandItem key="empty-placeholder" value="" disabled>No options available</CommandItem>];

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <button
          role="combobox"
          aria-expanded={open}
          className={cn(
            "flex h-10 w-full items-center justify-between rounded-md border border-input bg-background px-3 py-2 text-sm ring-offset-background placeholder:text-muted-foreground focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2 disabled:cursor-not-allowed disabled:opacity-50",
            triggerClassName
          )}
        >
          {value || <span className="text-muted-foreground">{placeholder}</span>}
          <ChevronDown className="h-4 w-4 opacity-50" />
        </button>
      </PopoverTrigger>
      <PopoverContent className={cn("p-0", className)} align="start" side="bottom">
        <Command className="w-full">
          <CommandInput
            placeholder="Search..."
            value={searchQuery}
            onValueChange={setSearchQuery}
            className="h-9"
          />
          <CommandEmpty>{emptyMessage}</CommandEmpty>
          <CommandGroup className="max-h-[200px] overflow-y-auto">
            {commandItems}
          </CommandGroup>
        </Command>
      </PopoverContent>
    </Popover>
  );
};

